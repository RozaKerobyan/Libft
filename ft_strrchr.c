#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen((char *)s) - 1;
	while (i >= 0)
	{
		if (s[i] == (char)c)
		{
			return ((char *)&s[i]);
		}
		i--;
	}
	if ((char)c == '\0')
	{
		return ((char *)&s[ft_strlen((char *)s)]);
	}
	return (NULL);
}
